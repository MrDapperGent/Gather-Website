<h2><a id="Tasks"></a>Game Modes</h2>
</div> <!-- /blue -->
<div class="content">
<p>1. Deathmatch</p>
    Everybody fights against each other. The one who kills the most wins.
Recommended Respawn Time: 3-6 seconds.
 

<p>2. Pointmatch [erase pls]</p>
    The same as Deathmatch but here you fight for points. There is a yellow flag on the map, when you carry it you double your points for kill. You can also get points for multikills. For killing 2 guys in a row you get double points, when you kill 3 you get 4 times more points. If you carry the flag and kill 6 guys in a row you can get 64 points!
Recommended Respawn Time: 3-6 seconds.</p>


<p>3. Rambomatch</p>
<p>Rambo - First Blood style. On the map there's John Rambo's Bow. The one who takes it gains super powers and can regenerate health. The rest of the players hunt him for the bow because only Rambo gets points for kill.
Recommended Respawn Time: 6-10 seconds.</p>


<p>4. Teammatch</p>
<p>There can be 4 teams on the map (Alpha Team, Bravo Team, Charlie Team, Delta Team) that fight against each other. The team that gets the highest score (sum of all kills) wins.</p>
 

<p>5. Capture the Flag</p>
<p>A competition for two teams (Alpha Team & Bravo Team). To score you have to steal the opponents team flag and bring to your base with your flag.</p>
 

<p>6. Infiltration</p>

<p>There are two teams (Alpha Team and Beta Team). The blue team has a base with a black flag, that they must defend.
 The goal of the red team is to steal the black flag and bring it to the white flag (usually near the reds spawn place). Recommended Realistic Mode.</p>

<p>7. Hold the flag [hide it]</p>

<p>Two teams (Alpha Team and Beta Team) fight over the yellow flag. The team that holds the flag gets points every couple of seconds.</p>
