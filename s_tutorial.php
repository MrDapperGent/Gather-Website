<h2><a id="Roles"></a>Personal Gamestyle Roles</h2>
</div> <!-- /blue -->
<div class="content">
<p>• <b>Rusher:</b> The rusher of the team will usually dive straight into the enemy's base in attempt to snatch their flag and score for the team,
 sometimes even completely ignoring enemies just in order to get that capture. However, it might be considerably smarter to try eliminate enemies whilst
 still trying to maintain speed on the rush to the flag. Note also that sometimes your teammates will need cover for potential enemy leaks. So keep your
 eyes on your base with peripheral vision even if your role is to rush. Rush can be done pretty much with any weapon. However there's the M79 and Spas
 which their boosting feature allow faster movement across the maps.</p>

<p>• <b>Defender:</b> The defender of the team will make sure to eliminate any enemies that his teammates failed to killed (leak).
 A defender position is needed in most maps. That being said, it is not highly recommended to play super defensive bunker style for 10
 minutes every map, because a lot of those small maps which are played are about applying constant pressure. If you're the defender of
 the team, try to find a balance and develop a sense of when can you attack and when you must defend. Defenders usually play automatic weapons,
 because the clip-sizes allow them to eliminate multiple enemies.</p>

<p>• <b>All Rounder:</b> Basically a combination of a rusher and defender.
 Such a player can provide the deathmatch when needed, and use momentum to his advantage in order to steal a cap.</p>

<p>Within time you'll realize what style suits you more, however being flexible and prone to the situational changes is probably the best thing you can do.</p>

