<h2><a id="Hints"></a>Hints</h2>
</div> <!-- /blue -->
<div class="content">
<p>You can change various settings to improve your performance. This starts from setting the controls to changing the games config to your needs over to applying mods.</p>
<center><u>Config Hints:</u></center>
<p>You can find 'config.exe' in Soldat's installation folder, from where you can change various options and game settings.</p>
<p>Recommended settings:</p>
<li>Graphic &#8250; 'Resolution' &#61; Desktop</li>
<li>Graphic &#8250; 'FPS Limit' &#61; ON &#61;400</li>
<li>Performance &#8250; 'Render weather effects' &#61; OFF (disables the distractive rain, snow & sandstorm weather effects)</li>
<li>Performance &#8250; 'Render background scenery' &#61; OFF (turns off distractive background sceneries on maps)</li>

<p><center><u>Ingame Hints:</u></center></p>

<li>After starting Soldat.exe navigate to the 'Player' tab and turn on 'Force background' and select a color, which will now be the default background color for all maps. This may improve your focus on enemies and bullets and can also
make it less stressing for your eyes.</li>

<li>Increase game volume ingame by pressing 'SHIFT' and '+'.</li>
<li>Decrease game volume ingame by pressing 'SHIFT' and '-'.</li>
<li>Change secondary weapon faster by pressingt 'CTRL' and '1'-'4' (e.g. 'CTRL' and '3' will select the knife)</li>
<li>You can view your FPS and other information by pressing 'ALT' and 'F3'.</li>
<li>Press 'F8' to <a href="#demo">record a demo</a> or press 'F4' to take a screenshot.</li>
<li>Press 'TAB' to activate or deactivate the weapons menu.</li>

<p><center><u>Controls:</u></center></p>

<p>Soldat's default controls are really bad and far away from an ergonomic experience. That results in players struggling to execute certain actions simultaneously and slows down their movement.</p>
<p>You should make sure that the following actions can easily be accessed:</p>
<li>move left &nbsp;&nbsp;&nbsp;&#8212; recommended key: A (default)</li>
<li>move right &#8212; recommended key: D (default)</li>
<li>jump &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#8212; recommended key: W (default)</li>
<li>lay down &nbsp;&nbsp;&nbsp;&#8212; recommended key: X or C</li>
<li>crouch &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#8212; recommended key: S</li>
<li>grenade &nbsp;&nbsp;&nbsp;&nbsp;&#8212; recommended key: SPACE</li>
<li>throw flag &nbsp;&#8212; recommended key: CTRL-L</li>
<li>drop weapon / throw knife &#8212; recommended key: SHIFT</li>


<p>Choosing from the control keys above will certainly improve your time to react and execute actions.</p>

