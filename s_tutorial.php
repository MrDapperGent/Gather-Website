<h2>About the Gamemode:</h2>
<p>This game-mode consists of two teams: Alpha (Red) & Bravo (Blue). The goal is for each team to steal the enemy's flag and bring it to your own flag. The team with more captures (caps) in the round are the winners.</p>
<p>There are 32 default CTF maps in Soldat, as well as hundreds of custom made maps.</p>
You should know each default map to succeed in competitive matches. You can get basic map knowledge
 by playing on CTF public servers.</p><p>Tip: You can test the maps offline in singleplayer mode (with no players) to explore map details.</p>
<p>Not all of the default maps are picked frequently in competetive matches.</p>
<p>The most favorized and popular maps for competetive CTF matches are: ctf_Guardian, ctf_Rotten, ctf_Wretch & ctf_IceBeam.</p> <p>The maps above are very popular due to its fast paced gameplay and its perfect layout for 3 vs. 3 matches.
 You will gain advanced map knowledge by playing them frequently in gathers or clanwars.</p>
