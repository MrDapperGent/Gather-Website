<h2><a id="Mistakes"></a>Known Mistakes</h2>
</div> <!-- /blue -->
<div class="content">
<p>1) Old Dog, New Tricks - Do not be afraid to learn something new (this one is dedicated to all the oldies out there).
 Yes, some of us having been playing over a decade now, but it doesn’t mean that we’re always open-minded. Playing 15 years
 doesn’t negate the fact that there might be something you missed, or something that you haven’t developed enough. Whether
 it’s movement, aiming, game-sense or whichever other aspect comes to mind. Try to keep an open-mind, and use losing, and
 observing the enemies’ actions, as a learning process. If you’ll do that you’ll be able to enjoy the game more, and get better at it as well. Double profit.</p>

<p>2) Leaking - Leaking can mean you completely missed an enemy due to not scanning the areas whilst on the go towards enemy base.
 It can also mean that you've failed a 1vs1 and failed to communicate it out to your team with taunts, so they can try to cover the route. </p>

<p>A good way to avoid leaks is to try to keep peripheral vision and active awareness of your surroundings. If you're sure an enemy is not approaching,
drag your crosshair around the corners of the map to see those blind spots and make sure you haven't missed an enemy (even if your teammates did not
acknowledge you in team-chat). Also in every 1vs1 that you fail, use the respective taunt to hint your team-mates which path to cover.</p>

<p>3) Ignoring Info - Team chat, Taunts and Radio Menu (for Radio use [V] button) are there for a reason. It's extremely important to keep communicating
 with the team during the match.</p>

<p>Soldat has key-bindings (taunts) to send team-chat-messages in a fast way. This allows you to give your team the needed information such as where the
 “EFC” (Enemy Flag Carrier) or the “FFC” (Friendly Flag Carrier) are located. You can assign those 'taunts' to ALT + [1,2,3...A,B,C...]. Get the Soldat Taunt
 Editor tool to create or customize taunts.</p>

<p>Make sure to use the information your team provides in order to enhance teamplay. Also make sure to deliver the CORRECT info to your team.
 If you mistakenly deliver 2 locations of enemy flag carrier, it might confuse your teammates and at times even harm the team's performance.</p>

<p>4) “Doubling” a route or not manning your route - As mentioned above, the roles are a general guideline, to keep
 an understanding between the teammates, who does what in the match. In most cases, not manning your route might force
 your teammates to clean up after your “leak” and generally might cause a lack of flow due to them having to pursue other
 roles. It’s a very risky move and a player should be a hundred percent sure that’s the right time or situation for doubling, meaning
 that the route is either completely clear or there’s a teammate covering using that path for that specific round for you.</p>

<p>5) No focus on base - In many situations it is highly important to clear the base first, for example: in IceBeam, there's
 a potential for multiple caps in a short time due to the skeleton of the map). Make sure to clear the base unless you're in
 a position that forces you to rush out of there, or you see that your teammates are defusing the situation. If you're not sure
 they're handling it (you see they're very Low HP), stick around until the situation is dealt with.</p>

<p>6) Points greediness - It’s a common misconception in CTF to think that you can make up for your lack of performance
of the capturing aspect of the game with a good kills and death score statistics. However, remember that, in the end of the map,
the score team’s flags is the one that matters. Be mindful to your surroundings, cover your teammates in a way that’ll contribute 
to the match and not only your personal stats.</p>

<p>6) Use your weapons - Use your secondary weapon and your grenades! Each player is equipped with a secondary weapons (USSOCOM, Combat Knife, Chainsaw & M72 LAW).
 Each weapon is unique and can be used for different scenarios. You can switch to your secondary weapon by pressing [Q].</p>

<p>- The USSOCOM pistol is usually equipped with one-shot weapons (M79, Barrett).</p>

<p>- The Combat Knife is probably the most common secondary weapon, it can be thrown, very useful for spawn killing or instant-knife-kill upon respawn to prevent enemies from capturing the flag. 

<p>- The Chainsaw is perhaps the hardest secondary to use. Both for novice and experienced shooters, and especially against experienced players.
 You will need a good concept of movement to use it to your advantage.</p>

<p>- M72 LAW very good for defensive scenarios, lurking and spawn killing. You can aim to common spots and "blind shot" or use known ricochet spots to get those blind shots as well.
 It is sometimes used even is a preventive weapon in a way, for example: you get the flag in ctf_Dropdown and players are respawning and you push them back into spawn by 
hitting the poly near them. Then escape with the flag or throw flag to teammate.</p>

<p>- Frag grenades: Default respawn grants 1 grenade, and grenade kits grant a maximum of 3 grenades. Using grenades whilst shooting the enemy can provide a very quick kill with
 a minimal usage of bullets. It is a very effective and commonly used method. Another thing you can use grenades for is boosting on colliders to gain speed (probably should practise
 that as well outside of gather to prevent losing an excessive amount of health due to boosting fails).</p>

<p>Summary:</p>
<p>It might seem a lot of work, but once you get used to each on of these concepts. It can pretty much become autopilot-mode, and you’ll use these good habits without thinking twice.</p>

