<h2><a id="Movement"></a>Movement</h2>
</div> <!-- /blue -->
<div class="content">
<p>Soldat has evolved in the past years and movement became a very important factor and the key to succeed. You will gain a lot of advantages if you learn the tricks below. They allow you to move very fast and precise. You can also dodge bullets.</p>
<p>To the contrary of how the movement system in most modern games work, Soldat has a very unique and complex movement system.</p>
<p>Similar to games like Quake you can find players doing a lot of movement-related tricks in Soldat (bunnyhopping, boosting & more).</p>

<p><u><center>The Kickjump:</p></u></center> You will use this whenever you want to go up a 'hill' fast.
<p>What you want to do is run up the hill, while (mashing) the jump button. I say (mashing) because there is an optimal rhythm,
 you don’t want to tap it too fast, you will have to practice to find the best time to spend with jump depressed and jump undepressed (better word?),
 but don’t go counting, even if you can count tenths of a second, just practice and you should get a feel for it over time. At the top of the hill press and hold jump without a direction.</p>
This is what it should look like:
<p><center><img src="http://i225.photobucket.com/albums/dd230/chutem/kickjump-jump.gif" alt="Gif1"></center></p>
 
<p><u><center>Reverse Backflip:</p></u></center> The reverse backflip is similar to the basic backflip.
This time you <li>start facing the same direction you are going</li> <li>while holding a direction button, press jump</li>
<li>then quickly change the direction button and right click</li> <li>All this time you must HOLD the jump button, which is key to most backflips as you will learn.</li></p>
<p><center><img src="http://i225.photobucket.com/albums/dd230/chutem/reverse-backflip.gif" alt="Gif1"></center></p>
<i>Useful for changing direction in certain situations.</i>

<p><u><center>Superman:</p></u></center> Superman allows you to get good lateral movement in mid air, it is better than standing and holding a direction key.
<p>Superman is very easy, just lie down and start jetting, if your jets run out in mid air, you want to stand up and hold a direction key, as it is better than letting your jets load and then jetting again.</p>
<p><center><img src="http://i225.photobucket.com/albums/dd230/chutem/superman.gif" alt="Gif1"></p>
<p>Superman can be effective after a kickjump, but not if you do a regular jump. This means doing a jump to the side and supermanning is the best.</p>
<p><center><img src="http://i225.photobucket.com/albums/dd230/chutem/kickjump-superman.gif" alt="Gif1"></center></p>


<p><u><center>Canonball:</p></u></center>
<p>The cannonball is the best single jump that will get you moving fast, quickly. It can also be a pain to learn and master,
 and also describe. But if you are to learn anything, this would be the best thing to learn. FUN FACT: Some people call the cannonball simply cball, for ease of typing.</p>
<li>First you must press a movement button and look in the opposite direction</li> <li>next you must tap jump and straight after tapping jump</li>
<li>you must right click</li>
<p>Be careful to not overlap the tapping of jump and the click. You should get this:</p>
<p><center><img src="http://imgur.com/SRjfI.gif" alt="Gif1"></center></p>
<i>The cannonball is very good for moving fast over relatively flat areas, by stringing them together.</i>

<p><center><img src="http://i225.photobucket.com/albums/dd230/chutem/kickjump-cannonball.gif" alt="Gif1"></p>
